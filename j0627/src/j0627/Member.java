package j0627;

public class Member {
	String name;
	int point;
	
	Member(){}
	Member(String name, int point){
		this.name = name; this.point = point;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPoint() {
		return point;
	}
	public void setPoint(int point) {
		this.point = point;
	}
	
	
	
	
}
